#Podstawowe typy zmiennych:

# 1. int - liczby całkowite (bez przecinka)
liczba = 10
print(f"Zmienna: {liczba} jest typu", type(liczba))
liczba = -50
print(liczba, type(liczba))

# 2. float - liczby zmiennoprzecinkowe
zmiennoprzecinkowa = 50.5
print(f"Zmienna: {zmiennoprzecinkowa} jest typu", type(zmiennoprzecinkowa))
zmiennoprzecinkowa = -5.5
print(zmiennoprzecinkowa, type(zmiennoprzecinkowa))

# 3. bool - Prawda lub Fałsz
prawda = True
print(f"Zmienna: {prawda} jest typu", type(prawda))
falsz = False
print(falsz, type(falsz))

# 4. string - tekst, ciąg znaków
tekst = "Mój napis"
print(f"Zmienna: {tekst} jest typu", type(tekst))
tekst = 'Kolejny napis'
print(tekst, type(tekst))
tekst = "23"
print(tekst, type(tekst))
tekst = "a"
print(tekst, type(tekst))
